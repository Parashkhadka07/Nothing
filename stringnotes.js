// // String Creation

// // Using Quotes: "Hello, World!" - Creates a string.
// // Using String Object: new String("Hello, World!") - Creates a string object.
// // String Length

// // str.length - Gets the length of the string.
// // Accessing Characters

// // By Index: str[index] - Accesses the character at the given index.
// // Using charAt: str.charAt(index) - Returns the character at the specified index.

// // String Methods

// // Concatenation: str1.concat(str2) - Concatenates str2 to str1.
// // Includes: str.includes(substring) - Checks if substring is present in str.
// // Starts With: str.startsWith(substring) - Checks if str starts with substring.
// // Ends With: str.endsWith(substring) - Checks if str ends with substring.
// // Index Of: str.indexOf(substring) - Returns the index of the first occurrence of substring.
// // Last Index Of: str.lastIndexOf(substring) - Returns the index of the last occurrence of substring.
// // Substring: str.substring(startIndex, endIndex) - Returns a substring from startIndex to endIndex.
// // Slice: str.slice(startIndex, endIndex) - Extracts a section of str and returns it as a new string.
// // Split: str.split(separator) - Splits str into an array of substrings.
// // Replace: str.replace(substring, newSubstring) - Replaces substring with newSubstring.
// // To Lower Case: str.toLowerCase() - Converts str to lower case.
// // To Upper Case: str.toUpperCase() - Converts str to upper case.
// // Trim: str.trim() - Removes whitespace from both ends of str.
// // Template Literals

// // Multi-line Strings: `Hello,\nWorld!` - Creates a multi-line string.
// // Interpolation: `Hello, ${name}!` - Embeds expressions in a string.
// // String Searching

// // Match: str.match(regex) - Searches str for matches using a regular expression.
// // Search: str.search(substring) - Searches str for substring and returns its position.
// // Includes: str.includes(substring) - Checks if substring is present in str.
// // Regular Expressions

// // Test: regex.test(str) - Tests if str matches the regular expression regex.
// // Exec: regex.exec(str) - Executes a search for a match in str and returns the match result.

// ///charAt()

// Purpose: Returns the character at a specified index.
// Syntax: str.charAt(index)
// charCodeAt()

// Purpose: Returns the Unicode value of the character at a specified index.
// Syntax: str.charCodeAt(index)
// concat()

// Purpose: Combines two or more strings.
// Syntax: str1.concat(str2, ..., strN)
// includes()

// Purpose: Checks if a string contains a specified substring.
// Syntax: str.includes(substring, position)
// endsWith()

// Purpose: Checks if a string ends with a specified substring.
// Syntax: str.endsWith(substring, length)
// indexOf()

// Purpose: Returns the index of the first occurrence of a specified substring.
// Syntax: str.indexOf(substring, fromIndex)
// lastIndexOf()

// Purpose: Returns the index of the last occurrence of a specified substring.
// Syntax: str.lastIndexOf(substring, fromIndex)
// localeCompare()

// Purpose: Compares two strings in the current locale.
// Syntax: str.localeCompare(compareString)
// match()

// Purpose: Searches a string for a match against a regular expression and returns the matches.
// Syntax: str.match(regex)
// matchAll()

// Purpose: Returns an iterator of all results matching a string against a regular expression.
// Syntax: str.matchAll(regex)
// padEnd()

// Purpose: Pads the current string with another string until the resulting string reaches the given length.
// Syntax: str.padEnd(targetLength, padString)
// padStart()

// Purpose: Pads the current string with another string (from the start) until the resulting string reaches the given length.
// Syntax: str.padStart(targetLength, padString)
// repeat()

// Purpose: Returns a new string with a specified number of copies of the original string.
// Syntax: str.repeat(count)
// replace()

// Purpose: Replaces occurrences of a substring or pattern with a new substring.
// Syntax: str.replace(substringOrRegex, newSubstringOrFunction)
// replaceAll()

// Purpose: Replaces all occurrences of a substring or pattern with a new substring.
// Syntax: str.replaceAll(substringOrRegex, newSubstringOrFunction)
// search()

// Purpose: Searches a string for a match against a regular expression and returns the index of the match.
// Syntax: str.search(regex)
// slice()

// Purpose: Extracts a section of a string and returns it as a new string.
// Syntax: str.slice(startIndex, endIndex)
// split()

// Purpose: Splits a string into an array of substrings.
// Syntax: str.split(separator, limit)
// startsWith()

// Purpose: Checks if a string starts with a specified substring.
// Syntax: str.startsWith(substring, position)
// substring()

// Purpose: Returns a subset of a string between two indices.
// Syntax: str.substring(startIndex, endIndex)
// toLowerCase()

// Purpose: Converts a string to lower case.
// Syntax: str.toLowerCase()
// toUpperCase()

// Purpose: Converts a string to upper case.
// Syntax: str.toUpperCase()
// trim()

// Purpose: Removes whitespace from both ends of a string.
// Syntax: str.trim()
// trimStart() / trimLeft()

// Purpose: Removes whitespace from the beginning of a string.
// Syntax: str.trimStart() / str.trimLeft()
// trimEnd() / trimRight()

// Purpose: Removes whitespace from the end of a string.
// Syntax: str.trimEnd() / str.trimRight()
// toLocaleLowerCase()

// Purpose: Converts a string to lower case according to the host’s current locale.
// Syntax: str.toLocaleLowerCase()
// toLocaleUpperCase()

// Purpose: Converts a string to upper case according to the host’s current locale.
// Syntax: str.toLocaleUpperCase()
// valueOf()

// Purpose: Returns the primitive value of a String object.
// Syntax: str.valueOf()
// normalize()

